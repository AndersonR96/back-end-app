    {
        request()->validate([
            'departament' => 'required', 
        ]);

        $created = Departament::create([
            'departament' => request('departament')
        ]);
        
        if($created){
            return redirect()->route('departaments.index')->with('creado');
        }else{
            return "no se ha podido crear";
        };
        
    }

